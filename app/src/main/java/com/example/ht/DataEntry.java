package com.example.ht;

import java.util.Date;

public class DataEntry {

    private float weight;
    private Date date;

    public DataEntry() {
    }
    public float getWeight() {
        return weight;
    }

    public void setWeight(float weight) {
        this.weight = weight;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }

}
